package com.pjw.day0528_operators;

/*
 * [비교연산자]
 *
 * 비교연산자는 결과가 true 또는 false로 나온다.
 */
public class Ex2_oper3 {

    public static void main(String[] args) {

        int x = 10;
        int y = 3;

        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        System.out.println(x == y);
        System.out.println(x != y);
    }
}
